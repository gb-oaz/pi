package com.pi.core_auth.core.utils.constants;

/**
 * A utility class that defines constant values for various command types.
 * <p>
 * These constants are used throughout the application to represent specific
 * commands, such as login, code verification, and password-related operations.
 * </p>
 */
public class Command {

    /**
     * Represents the "login" command.
     */
    public static final String LOGIN = "login";

    /**
     * Represents the "code" command, typically used for code verification.
     */
    public static final String CODE = "code";

    /**
     * Represents the "password" command, typically used for password-related operations.
     */
    public static final String PASSWORD = "password";

    /**
     * Represents the "anonymousToken" command, typically used for anonymous token operations.
     */
    public static final String ANONYMOUS_TOKEN = "anonymousToken";
}